//! Virtual wheel HID report descriptor and input report layout.

pub const VENDOR: u16 = 0x046d;
pub const PRODUCT: u16 = 0xc276;
pub const NAME: &str = "Logitech RS50 Base for PlayStation/PC";
pub const INPUT_REPORT_ID: u8 = 0x01;
pub const INPUT_REPORT_LEN: usize = 14;

#[derive(Debug, Default, Clone, Copy, PartialEq, Eq)]
pub struct InputReport {
    pub steering: u16,
    pub throttle: u16,
    pub brake: u16,
    pub clutch: u16,
    pub buttons: u32,
    pub hat: u8,
}

impl InputReport {
    pub fn to_bytes(&self) -> Vec<u8> {
        let mut b = Vec::with_capacity(INPUT_REPORT_LEN);
        b.push(INPUT_REPORT_ID);
        b.extend_from_slice(&self.steering.to_le_bytes());
        b.extend_from_slice(&self.throttle.to_le_bytes());
        b.extend_from_slice(&self.brake.to_le_bytes());
        b.extend_from_slice(&self.clutch.to_le_bytes());
        b.extend_from_slice(&self.buttons.to_le_bytes());
        b.push(self.hat);
        b
    }
}

