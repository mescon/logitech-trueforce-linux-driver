const APP_END: &[u8] = &[0xC0]; // End Collection (Application)

/// Full report descriptor: joystick application collection (input report id 1)
/// followed by the PID output collection, then the application End Collection.
pub fn report_descriptor() -> &'static [u8] {
    use std::sync::OnceLock;
    static D: OnceLock<Vec<u8>> = OnceLock::new();
    D.get_or_init(|| {
        let mut v = Vec::new();
        v.extend_from_slice(JOYSTICK_PREFIX);
        v.extend_from_slice(PID_COLLECTION);
        v.extend_from_slice(APP_END);
        v
    })
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn input_report_serializes_report_id_and_axes_little_endian() {
        let r = InputReport { steering: 0x1234, throttle: 0x00ff, brake: 0, clutch: 0, buttons: 0b101, hat: 0x08 };
        let b = r.to_bytes();
        assert_eq!(b[0], INPUT_REPORT_ID);          // report id first
        assert_eq!(&b[1..3], &[0x34, 0x12]);         // steering LE
        assert_eq!(&b[3..5], &[0xff, 0x00]);         // throttle LE
        assert_eq!(b.len(), INPUT_REPORT_LEN);
    }

    #[test]
    fn report_descriptor_is_wellformed() {
        let d = report_descriptor();
        // Balanced collections: every 0xA1 (Collection) has a matching 0xC0 (End).
        let opens = d.iter().filter(|&&b| b == 0xA1).count();
        let closes = d.iter().filter(|&&b| b == 0xC0).count();
        assert_eq!(opens, closes, "unbalanced HID collections");
        // Input report id 1 and PID Set-Effect report id 0x51 both present as `0x85, id`.
        assert!(d.windows(2).any(|w| w == [0x85, INPUT_REPORT_ID]));
        assert!(d.windows(2).any(|w| w == [0x85, 0x51]));
    }
}
